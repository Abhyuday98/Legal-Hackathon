                    <p>Other regulations that govern business practices include:</p>
                    -Penal code for criminal breaches in business conduct<br>
                    -Dishonest misappropriation of property (S403)<br>
                    -Criminal breach of trust (S405)<br>
                    -Accounting and Corporate Regulatory Authority (ACRA)<br><br>
                    -Uses legislations including but not limited to:<br>
                    -Companies (Amendment) Act  2017<br>
                    -Business Names Registration Act<br>


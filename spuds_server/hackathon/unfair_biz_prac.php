                <div class="panel panel-primary">
                    <div class="panel-heading">Consumer Protection (Fair Trading) Act (CPFTA)</div>
                    <div class="panel-body">
                    <p>Consumer laws in Singapore are based on the The Consumer Protection (Fair Trading) Act (CPFTA) is a law protecting consumers in Singapore. While it applies to most consumer transactions, it does not apply to sales of land and houses, employment contracts, and pawnbroking. It includes the following:</p>

                    -Definition of unfair practices (s6 CPFTA)<br>
                    -Examples of unfair practices (Second Schedule of CPFTA)<br>
                    -The action must be commenced within 2 years of the latest occurrence of the last material event where the unfair practice happened or when the consumer first discovers it

                    </div>
                </div>
                
                <div class="panel panel-success">
                    <div class="panel-heading">
                    <h3 class="panel-title">Unfair Contract Terms Act (UCTA)</h3>
                    </div>
                    <div class="panel-body">
                        <p>Business practices in contracts are regulated by the Unfair Contract Terms Act (UCTA). It allows for the following:</p>
                        -A person cannot use a contract to exclude liability for negligence that causes death or personal injury (s2(1) UCTA)<br>
                        -A person can use a contract to limit or exclude his liability for his negligence that causes other losses or property damage, so far as it is reasonable (s2(2) UCTA)<br>
                        -A person cannot be forced to indemnify another person because of liability for a negligence that was not caused by him (s4 UCTA)<br>
                        The test for the reasonableness of a contractual term (s11 UCTA)
                        
                    </div>
                </div>

                <div class="panel panel-info">
                    <div class="panel-heading">
                    <h3 class="panel-title">Other regulations</h3>
                    </div>
